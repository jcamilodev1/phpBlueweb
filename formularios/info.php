Como se pueden crear distintos usuarios de una app y que por cada usuario se cargue distinta informacion en php?
    Creo que se hace una consulta y se traen las tablas de que solo tienen ese id. En todo caso en el tema del select tenemos que en vez del asterisco se escribe el id del usuario en cuestion y ahì se traen solo los datos de ese usuario. Y tambien se hace una consulta a la base de datos donde se usan las tablas solo con el id de esa persona. Cuando se crea un usuario se crea en la base de tados una tabla o varias tablas con los datos de esa persona y que solo esa persona puede acceder.
